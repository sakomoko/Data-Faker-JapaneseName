my $names = YAML::Load(<DATA>);
__PACKAGE__->register_plugin(
	japanese_family_name => sub {
			my @data = split(/<>/, $names->[rand(@$names)]);
			shift @data;
	},
	japanese_family_name_hash => sub {
		my @data = split(/<>/, $names->[rand(@$names)]);
		{
			'kanji' => $data[0],
			'kana' => $data[1],
		};
	},
);

